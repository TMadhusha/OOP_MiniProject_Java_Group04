    private void AdminFgtpawd5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdminFgtpawd5ActionPerformed

        try {
            DatabaseConnection db = new DatabaseConnection();
            String query;
            PreparedStatement ps;

            query = "delete from attendance where at_st_id = ? and at_date = ? and at_cou_id = ?";
            ps = db.getCon().prepareStatement(query);
            ps.setString(1, txtStudentId.getText());
            ps.setString(2, txtDate.getText());
            ps.setString(3, txtCourseId.getText());

            ps.executeUpdate();
            db.getCon().close();
            loadData();
            txtSearchId.setText("");
            JOptionPane.showMessageDialog(this, "Record deleted successfully");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_AdminFgtpawd5ActionPerformed

